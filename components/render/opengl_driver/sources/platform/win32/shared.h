///Создание и удаление контекста
///////////////////////////////////////////////////////////////////////////////////////////////////
    virtual HGLRC CreateContext (HDC dc) = 0;
    virtual void  DeleteContext (HGLRC context) = 0;

///////////////////////////////////////////////////////////////////////////////////////////////////
