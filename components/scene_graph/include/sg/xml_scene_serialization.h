    ScenePrototype& operator = (const ScenePrototype&);

///////////////////////////////////////////////////////////////////////////////////////////////////
///Группа ресурсов
///////////////////////////////////////////////////////////////////////////////////////////////////
    const scene_graph::ResourceGroup& Resources     () const; //группа ресурсов данного прототипа
