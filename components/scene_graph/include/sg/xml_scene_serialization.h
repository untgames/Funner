///////////////////////////////////////////////////////////////////////////////////////////////////
///Добавление и удаление дочерних прототипов
///////////////////////////////////////////////////////////////////////////////////////////////////
    size_t AddChild          (const ScenePrototype&);
    void   RemoveChild       (size_t index);
    void   RemoveAllChildren ();
    
