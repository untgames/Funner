#ifndef MEDIALIB_SPINE_SLOT_HEADER
#define MEDIALIB_SPINE_SLOT_HEADER

#include <math/vector.h>

namespace media
{

namespace spine
{

//forward declarations
class Attachment;
class Bone;
class SlotImpl;
class Wrappers;

///////////////////////////////////////////////////////////////////////////////////////////////////
///Spine slot
///////////////////////////////////////////////////////////////////////////////////////////////////
class Slot
{
  friend class Wrappers;
  public:
///////////////////////////////////////////////////////////////////////////////////////////////////
///Constructors / destructor / assignment
///////////////////////////////////////////////////////////////////////////////////////////////////
    Slot (const Slot&);
    ~Slot ();

    Slot& operator = (const Slot&);

///////////////////////////////////////////////////////////////////////////////////////////////////
///Name
///////////////////////////////////////////////////////////////////////////////////////////////////
    const char* Name () const;

///////////////////////////////////////////////////////////////////////////////////////////////////
///Params, returned object is owned by callee.
///////////////////////////////////////////////////////////////////////////////////////////////////
    math::vec4f               Color () const;  //only color parameter available, darkColor parameter not available
    const media::spine::Bone* Bone  () const;

///////////////////////////////////////////////////////////////////////////////////////////////////
///Attachment, returned object is owned by callee.
///////////////////////////////////////////////////////////////////////////////////////////////////
          media::spine::Attachment* Attachment ();
    const media::spine::Attachment* Attachment () const;

///////////////////////////////////////////////////////////////////////////////////////////////////
///Swap
///////////////////////////////////////////////////////////////////////////////////////////////////
    void Swap (Slot&);

  protected:
    Slot (SlotImpl*);

  private:
    SlotImpl* impl;
};

///////////////////////////////////////////////////////////////////////////////////////////////////
///Swap
///////////////////////////////////////////////////////////////////////////////////////////////////
void swap (Slot&, Slot&);

}

}

#endif
