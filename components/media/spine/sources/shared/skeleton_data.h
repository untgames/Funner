#ifndef MEDIALIB_SPINE_SHARED_SKELETON_DATA_HEADER
#define MEDIALIB_SPINE_SHARED_SKELETON_DATA_HEADER

#include <media/spine/skeleton_data.h>

#include <object.h>

namespace media
{

namespace spine
{

//forward declarations
class ISkeleton;
class IAnimationStateData;

///////////////////////////////////////////////////////////////////////////////////////////////////
///Spine skeleton data
///////////////////////////////////////////////////////////////////////////////////////////////////
class ISkeletonData : virtual public IObject
{
  public:
///////////////////////////////////////////////////////////////////////////////////////////////////
///Create object instances
///////////////////////////////////////////////////////////////////////////////////////////////////
    virtual ISkeleton*           CreateSkeleton           () = 0;
    virtual IAnimationStateData* CreateAnimationStateData () = 0;

///////////////////////////////////////////////////////////////////////////////////////////////////
///Params
///////////////////////////////////////////////////////////////////////////////////////////////////
    virtual float Width  () = 0;
    virtual float Height () = 0;

///////////////////////////////////////////////////////////////////////////////////////////////////
///Animations
///////////////////////////////////////////////////////////////////////////////////////////////////
    virtual unsigned int AnimationsCount   () = 0;
    virtual const char*  AnimationName     (unsigned int index) = 0;
    virtual float        AnimationDuration (unsigned int index) = 0;

///////////////////////////////////////////////////////////////////////////////////////////////////
///Skins
///////////////////////////////////////////////////////////////////////////////////////////////////
    virtual unsigned int SkinsCount      () = 0;
    virtual const char*  SkinName        (unsigned int index) = 0;
    virtual const char*  DefaultSkinName () = 0;
};

}

}

#endif
